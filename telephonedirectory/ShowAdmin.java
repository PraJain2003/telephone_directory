    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        //udpate button
        try
        {
            MyLib obj = new MyLib();
            Connection cn=obj.create_connection();
            String s1="update admindata set name=?,father=?,address=?,city=?,state=?,pincode=?,contact=? where email=?";
            String s2="update logindata set password=? where email=?";
            PreparedStatement p1=cn.prepareStatement(s1);
            PreparedStatement p2=cn.prepareStatement(s2);
            //replacing ? with textfields values
            p1.setString(1, jTextField1.getText().trim());
            p1.setString(2, jTextField2.getText().trim());
            p1.setString(3, jTextField3.getText().trim());
            p1.setString(4, jTextField4.getText().trim());
            p1.setString(5, jTextField5.getText().trim());
            p1.setString(6, jTextField6.getText().trim());
            p1.setString(7, jTextField7.getText().trim());
            p1.setString(8, jTextField8.getText().trim());

            p2.setString(2, jTextField8.getText().trim());

            int a =p1.executeUpdate();
            int b=p2.executeUpdate();

            if(a==1 && b==1)
            {
                javax.swing.JOptionPane.showMessageDialog(null, "Data Updated");
            }
            else
            {
                javax.swing.JOptionPane.showMessageDialog(null, "Data not Updated");
            }
        }
        catch(Exception e)
        {
            javax.swing.JOptionPane.showMessageDialog(null, ""+e);
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

