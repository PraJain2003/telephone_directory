    private void ShowButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowButtonActionPerformed
        //code to show data of related email
        try
        {
            MyLib obj = new MyLib();
            Connection cn=obj.create_connection();
            //instead of making 2 queries we extract data from view
            //String s1="select * from admindata where email=?";
            //String s2="select * from logindata where email=?";
            String sql="select * from admindata where email=?";
            PreparedStatement p1=cn.prepareStatement(sql);
            p1.setString(1, jTextField8.getText().trim());
            ResultSet rs=p1.executeQuery();
            if(rs.next())
            {
                String id,name,father,address,contact,city,state,pincode,email;
                id=rs.getString("id");
                name=rs.getString("name");
                father=rs.getString("father");
                address=rs.getString("address");
                contact=rs.getString("contact");
                city=rs.getString("city");
                state=rs.getString("state");
                pincode=rs.getString("pincode");
                email=rs.getString("email");

                //setting values according to email
                jTextField1.setText(name);
                jTextField2.setText(father);
                jTextField3.setText(address);
                jTextField4.setText(city);
                jTextField5.setText(state);
                jTextField6.setText(pincode);
                jTextField7.setText(contact);
                jTextField8.setText(email);

                //allowing buttons
                DeleteButton.setEnabled(true);
                UpdateButton.setEnabled(true);
                CancelButton.setEnabled(true);
                ShowButton.setEnabled(false);

                //setting editable and noneditable fields
                jTextField1.setEnabled(true);
                jTextField2.setEnabled(true);
                jTextField3.setEnabled(true);
                jTextField4.setEnabled(true);
                jTextField5.setEnabled(true);
                jTextField6.setEnabled(true);
                jTextField7.setEnabled(true);
                jTextField8.setEditable(false);
            }

        }
        catch(Exception e)
        {
            javax.swing.JOptionPane.showMessageDialog(null, ""+e);
        }
    }//GEN-LAST:event_ShowButtonActionPerformed

